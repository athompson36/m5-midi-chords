void uiThemeApply(uint8_t themeIndex) {
  if (themeIndex >= kUiThemeCount) {
    themeIndex = 0;
  }
  memcpy(&g_uiPalette, &kThemes[themeIndex], sizeof(UiPalette));
}

const char* uiThemeName(uint8_t themeIndex) {
  if (themeIndex >= kUiThemeCount) {
    return kNames[0];
  }
  return kNames[themeIndex];
}
